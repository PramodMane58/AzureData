import matplotlib.pyplot as plt
import numpy as np
x = [1,5,10,15]
y = [1,2,3,4]
plt.plot(x, y)
# Set the x axis label of the current axis.
plt.xlabel('x - axis')
# Set the y axis label of the current axis.
plt.ylabel('y - axis')
# Set a title
plt.title('Sample graph!')
# Display a figure.
plt.show()